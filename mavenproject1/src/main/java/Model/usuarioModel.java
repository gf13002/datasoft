package Model;


public class usuarioModel {
    private int idusr;
    private String codigo, nombre, clave;
    
    public usuarioModel() {
    }
    public usuarioModel(int idusr, String codigo, String nombre, String clave) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.clave = clave;
    }

    public int getIdusr() {
        return idusr;
    }

    public void setIdusr(int idusr) {
        this.idusr = idusr;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }
    
    
}
